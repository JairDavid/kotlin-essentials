fun vars(){
    //What's difference between val and var?
    //As in Java, val is equivalent to final this means that's inmmutable
    //For other hand, var can be mutated (change values like a normal datatype)

    //type inference
    val y = 10 //kind of constant
    var x = 5 //normal variable

    //more ways to declare variables
    var a: Int = 2
    var b: Float = 2F
}